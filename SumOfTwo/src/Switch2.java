
public class Switch2 {
 
		   
        public static void main(String[] args) {

            int secondNumber=3;
           

            // switch statement with int data type
            switch (secondNumber) {
                case 1:
                   System.out.println("Hey found number 1");
                    break;
                case 2:
                	 System.out.println("Hey found number 2");
                    break;
                case 3:
                	 System.out.println("Hey found number 3");
                    break;
                case 4:
                	 System.out.println("Hey found number 4");
                    break;
         
                default:
                	 System.out.println("Number not found");
              
           
        }
    }



}
